using System;

namespace MyLibrary
{
    public class MyLib
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public double Divide(int a, int b)
        {
            return (double)a / (double)b;
        }

        public double Multiple(int a, int b)
        {
            return a * b ;
        }
    }
}
